// slice :
let color=["red","purple","pink","blue","black"]

let c=color.slice(0,3)
console.log(c)

// push : adds new items to the end of an array
let array=["1","2","3","4","5"]
array.push("6","7")
console.log(array)

// pop : removes (pops) the last element of an array

let fruit=["apple","orange","pineapple","cherry","strawberry"]
fruit.pop();
console.log(fruit)

// shift :  removes the first item of an array
let num=["0","1","2","3","4"]
num.shift();
console.log(num)

// unshift : adds new elements to the beginning of an array.
let days=["tue","wed","thu","fri","sat"]
days.unshift("sun","mon")
console.log(days)

// sort : show the array in alphabetical order
let fruits=["Banana","orange","watermelon","lemon"]
fruits.sort();
console.log(fruits)

let sum=["3","1","0","4","2"]
sum.sort();
console.log(sum)


// reverse : reverses the order of the elements in an array. 
let fruitss=["Banana","orange","watermelon","lemon"]
fruitss.reverse();
console.log(fruitss)

